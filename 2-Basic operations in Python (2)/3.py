s = int(input())
n1 = " програмістів"
n2 = " програміст"
n3 = " програміста"
if s >= 0:
    if s == 0:
        print(str(s) + n1)  # програмістів(0)
    elif 10 <= s % 100 <= 20:
        print(str(s) + n1)  # програмістів(10<S<20)
    elif s % 10 == 1:
        print(str(s) + n2)  # програміст(непарне число)
    elif 2 <= s % 10 <= 4:
        print(str(s) + n3)  # програміста(2<S<4)
    else:
        print(str(s) + n1)  # програмістів(все інше)
